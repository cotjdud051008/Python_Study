### 주어진 영어 문장의 첫 글자는 대문자로, 나머지 글자는 소문자로 변환

# 주어진 문장 : the early bird catches the worm.

sen = 'the early bird catches the worm.'

print(sen.capitalize())

# 주어진 문장 : Actions Speak Louder Than Words.

sen = 'Actions Speak Louder Than Words.'

print(sen.capitalize())

# 주어진 문장 : PRACTICE MAKES PERFECT.

sen = 'PRACTICE MAKES PERFECT.'

print(sen.capitalize())

